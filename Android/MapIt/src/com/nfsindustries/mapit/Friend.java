package com.nfsindustries.mapit;

public class Friend {
	public String name;
	public String city;
	public double latitude;
	public double longitude;
	
	Friend(String theName, String theCity, double theLatitude, double theLongitude)
	{
		name = theName;
		city = theCity;
		latitude = theLatitude;
		longitude = theLongitude;
	}

}
